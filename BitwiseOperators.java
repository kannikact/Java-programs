class BitwiseOperators{
public static void main(String args[]){
int a=1,b=2,c,d,e,f,g,h,i;
c=a&b;
d=a|b;
e=a^b;
f=~35;
g=b<<1;
h=a>>1;
i=4>>>1;
System.out.println("the output is:" +c);
System.out.println("the output is:" +d);
System.out.println("the output is:" +e);
System.out.println("the output is:" +f);
System.out.println("the output is:" +g);
System.out.println("the output is:" +h);
System.out.println("the output is:" +i);
}
}